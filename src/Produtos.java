public class Produtos {
    public String name;
    public int codigoDePesagem;
    public double price;

    public Produtos() {
        this.name = name;
        this.codigoDePesagem = codigoDePesagem;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCodigoDePesagem() {
        return codigoDePesagem;
    }

    public void setCodigoDePesagem(int codigoDePesagem) {
        this.codigoDePesagem = codigoDePesagem;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}

